                <h3>工资、薪金所得</h3>
                <p><strong>个人所得税计算公式</strong></p>
                <p>应纳税所得额 = 税前工资收入金额 － 五险一金(个人缴纳部分) － 起征点(3500元)</p>
                <p>应纳税额 = 应纳税所得额 x 税率 － 速算扣除数</p>
                <p>提示：自2011年开始至今，内地人员个税起征点为3500元，外籍人士（包括港、澳、台），个税起征点应设为4800元。</p>
                <p><strong>举例：</strong></p>
