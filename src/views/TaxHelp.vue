                <p>那么：</p>
                <p>应纳税额 = 6000 - 6000 x (8% + 2% + 1% + 8%) - 3500 = 1360(元)</p>
                <p>查上表可知，与 1360 元对应的税率和速算扣除数分别为 3% 和 0，因此：</p>
                <p>个税 = 1360 x 3% - 0 = 40.8(元)</p>
